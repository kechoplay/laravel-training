    {
        //
        $data = $request->input('user');
        $user = new User();
        $user->name = $data['name'];
        $user->email = $data['email'];
        $user->password = bcrypt('123456');
        $user->save();

        $role  = Role::where('name', $data['role'])->first();
        $user->roles()->attach($role);
        return response(['success' => true, 'data' => $user]);
    }

